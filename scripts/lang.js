function initLang(lang) {
  const nodes = Array.from(document.querySelectorAll("[data-lang]"));

  document.documentElement.lang = lang;

  for (const node of nodes) {
    const translation = contents[node.id]?.[lang];
    if (!translation) continue;

    const attr = node.dataset.langAttr;
    if (attr) {
      if (node.getAttribute(attr) !== translation) {
        node.setAttribute(attr, translation);
      }
    } else if (node.textContent !== translation) {
      node.textContent = translation;
    }
  }

  document.dispatchEvent(new CustomEvent("languagechange", { detail: { lang } }));
}

function changeLang(lang) {
  initLang(lang);
}

const elements = document.getElementsByClassName("c-navbar__lang-link");

for (let i = 0; i < elements.length; i++) {
  elements[i].addEventListener("click", (event) => {
    event.preventDefault();
    changeLang(elements[i].id);
  });
}

window.addEventListener("DOMContentLoaded", () => initLang("en"));
